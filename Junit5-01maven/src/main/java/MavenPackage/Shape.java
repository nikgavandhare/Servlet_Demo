package MavenPackage;

public class Shape 
{
	public  double CalAreaCircle(double radius) 
	{
		return 3.14*radius*radius;
		
	}
	
	

	public  double CalAreaSquare(double len) 
	{
		return len*len;
		
	}
	

	public  double CalAreaRectangle(double hight,double width) 
	{
		return hight*width;
		
	}

}
